// 이름의 발음 자연스러움 체크
export function isNaturalCombination(first: string, second: string, gender: 'male' | 'female'): boolean {
  const data = NATURAL_NAMES[gender];
  
  // 조합 테이블에 있으면 자연스러움
  if (data.combinations[first]?.includes(second)) {
    return true;
  }
  
  // 인기 이름에 있으면 자연스러움
  if (data.popular.includes(first + second)) {
    return true;
  }
  
  // 둘 다 유효한 음절이면 허용 (약간 덜 자연스러울 수 있음)
  return data.first.includes(first) && data.second.includes(second);
}

// 특정 음절과 어울리는 두 번째 음절 찾기
export function getNaturalSecond(first: string, gender: 'male' | 'female'): string[] {
  const data = NATURAL_NAMES[gender];
  
  // 조합 테이블에서 먼저 찾기
  if (data.combinations[first]) {
    return data.combinations[first];
  }
  
  // 없으면 일반적인 두 번째 음절 반환
  return data.second;
}

// 인기 이름에서 랜덤 선택
export function getPopularName(gender: 'male' | 'female'): string {
  const names = NATURAL_NAMES[gender].popular;
  return names[Math.floor(Math.random() * names.length)];
}

// 첫 번째 음절에서 랜덤 선택
export function getRandomFirst(gender: 'male' | 'female'): string {
  const firsts = NATURAL_NAMES[gender].first;
  return firsts[Math.floor(Math.random() * firsts.length)];
}



